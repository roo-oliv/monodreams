using System;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;
using MonoGame.Extended.BitmapFonts;

namespace MonoDreams.Component;

public struct SimpleText
{
    private string _value;
    public string Value
    {
        get => _value;
        set
        {
            _value = value;
            Origin = new Vector2(
                x: HorizontalAlign switch
                {
                    HorizontalAlign.Left => 0,
                    HorizontalAlign.Center => Font.MeasureString(value).Width / 2,
                    HorizontalAlign.Right => Font.MeasureString(value).Width,
                    _ => throw new ArgumentOutOfRangeException(),
                },
                y: VerticalAlign switch
                {
                    VerticalAlign.Top => Font.MeasureString(value).Height,
                    VerticalAlign.Center => Font.MeasureString(value).Height / 2,
                    VerticalAlign.Bottom => 0,
                    _ => throw new ArgumentOutOfRangeException(),
                });
        }
    }

    public BitmapFont Font;
    public Color Color;
    public HorizontalAlign HorizontalAlign;
    public VerticalAlign VerticalAlign;
    public Enum DrawLayer;

    public SimpleText(string value, BitmapFont font, Color color, HorizontalAlign horizontalAlign, VerticalAlign verticalAlign, Enum drawLayer = null) : this()
    {
        Font = font;
        Color = color;
        HorizontalAlign = horizontalAlign;
        VerticalAlign = verticalAlign;
        Value = value;
        DrawLayer = drawLayer;
    }

    public Vector2 Origin { get; private set; }
}

public enum HorizontalAlign
{
    Left,
    Center,
    Right,
}

public enum VerticalAlign
{
    Top,
    Center,
    Bottom,
}
